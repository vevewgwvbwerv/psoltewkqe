-- –ì–ª–æ–±–∞–ª—å–Ω—ã–µ –ø–µ—Ä–µ–º–µ–Ω–Ω—ã–µ
local savedPetTool = nil
local animationConnection = nil

-- –ü–æ–∏—Å–∫ –ø–∏—Ç–æ–º—Ü–∞ –≤ —Ä—É–∫–∞—Ö
local function findPetInHands()
    local character = player.Character
    if not character then return nil end
    
    for _, tool in pairs(character:GetChildren()) do
        if tool:IsA("Tool") and string.find(tool.Name, "%[") and string.find(tool.Name, "KG%]") then
            return tool
        end
    end
    return nil
end

-- –ü–æ–∏—Å–∫ Shovel –≤ —Ä—É–∫–∞—Ö
local function findShovelInHands()
    local character = player.Character
    if not character then return nil end
    
    for _, tool in pairs(character:GetChildren()) do
        if tool:IsA("Tool") and (string.find(tool.Name, "Shovel") or string.find(tool.Name, "Destroy")) then
            return tool
        end
    end
    return nil
end

-- –°–û–•–†–ê–ù–ò–¢–¨ –ø–∏—Ç–æ–º—Ü–∞
local function savePet()
    print("\nüíæ === –°–û–•–†–ê–ù–ï–ù–ò–ï –ü–ò–¢–û–ú–¶–ê ===")
    
    local pet = findPetInHands()
    if not pet then
        print("‚ùå –ü–∏—Ç–æ–º–µ—Ü –≤ —Ä—É–∫–∞—Ö –Ω–µ –Ω–∞–π–¥–µ–Ω!")
        return false
    end
    
    print("‚úÖ –ù–∞–π–¥–µ–Ω –ø–∏—Ç–æ–º–µ—Ü: " .. pet.Name)
    savedPetTool = pet
    print("‚úÖ –ü–∏—Ç–æ–º–µ—Ü —Å–æ—Ö—Ä–∞–Ω–µ–Ω!")
    return true
end

-- –£–õ–¨–¢–†–ê-–ü–†–û–°–¢–ê–Ø –ó–ê–ú–ï–ù–ê: –ø—Ä–æ—Å—Ç–æ –º–µ–Ω—è–µ–º —Å–æ–¥–µ—Ä–∂–∏–º–æ–µ Shovel
local function ultraSimpleReplace()
    print("\nüî• === –£–õ–¨–¢–†–ê-–ü–†–û–°–¢–ê–Ø –ó–ê–ú–ï–ù–ê ===")
    
    if not savedPetTool then
        print("‚ùå –°–Ω–∞—á–∞–ª–∞ —Å–æ—Ö—Ä–∞–Ω–∏—Ç–µ –ø–∏—Ç–æ–º—Ü–∞!")
        return false
    end
    
    local shovel = findShovelInHands()
    if not shovel then
        print("‚ùå Shovel –≤ —Ä—É–∫–∞—Ö –Ω–µ –Ω–∞–π–¥–µ–Ω!")
        return false
    end
    
    print("‚úÖ –ù–∞–π–¥–µ–Ω Shovel: " .. shovel.Name)
    print("üîß –£–ª—å—Ç—Ä–∞-–ø—Ä–æ—Å—Ç–∞—è –∑–∞–º–µ–Ω–∞ —Å–æ–¥–µ—Ä–∂–∏–º–æ–≥–æ...")
    
    -- –®–ê–ì 1: –°–æ—Ö—Ä–∞–Ω—è–µ–º –ø–æ–∑–∏—Ü–∏—é Shovel
    local shovelHandle = shovel:FindFirstChild("Handle")
    local shovelPosition = nil
    if shovelHandle then
        shovelPosition = shovelHandle.CFrame
        print("üìç –°–æ—Ö—Ä–∞–Ω–µ–Ω–∞ –ø–æ–∑–∏—Ü–∏—è Shovel")
    end
    
    -- –®–ê–ì 2: –û—á–∏—â–∞–µ–º —Å–æ–¥–µ—Ä–∂–∏–º–æ–µ Shovel
    print("üßπ –û—á–∏—â–∞—é —Å–æ–¥–µ—Ä–∂–∏–º–æ–µ Shovel...")
    for _, child in pairs(shovel:GetChildren()) do
        if child.Name ~= "Handle" then
            child:Destroy()
        end
    end
    
    -- –®–ê–ì 3: –ö–æ–ø–∏—Ä—É–µ–º —Å–æ–¥–µ—Ä–∂–∏–º–æ–µ –ø–∏—Ç–æ–º—Ü–∞ –≤ Shovel
    print("üìã –ö–æ–ø–∏—Ä—É—é —Å–æ–¥–µ—Ä–∂–∏–º–æ–µ –ø–∏—Ç–æ–º—Ü–∞ –≤ Shovel...")
    for _, child in pairs(savedPetTool:GetChildren()) do
        if child.Name ~= "Handle" then
            local copy = child:Clone()
            copy.Parent = shovel
            print("   ‚úÖ –°–∫–æ–ø–∏—Ä–æ–≤–∞–Ω:", child.Name)
        end
    end
    
    -- –®–ê–ì 4: –ú–µ–Ω—è–µ–º –∏–º—è
    shovel.Name = "Dragonfly [6.36 KG] [Age 35]"
    print("‚úÖ –ò–º—è –∏–∑–º–µ–Ω–µ–Ω–æ –Ω–∞:", shovel.Name)
    
    -- –®–ê–ì 5: –í–æ—Å—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ–º –ø–æ–∑–∏—Ü–∏—é
    if shovelPosition and shovelHandle then
        shovelHandle.CFrame = shovelPosition
        print("üìç –ü–æ–∑–∏—Ü–∏—è –≤–æ—Å—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω–∞")
    end
    
    -- –®–ê–ì 6: –ù–∞—Å—Ç—Ä–∞–∏–≤–∞–µ–º Anchored (–≤—Å–µ —á–∞—Å—Ç–∏ –ù–ï –∑–∞—è–∫–æ—Ä–µ–Ω—ã –¥–ª—è –∞–Ω–∏–º–∞—Ü–∏–∏)
    print("üîß –ù–∞—Å—Ç—Ä–∞–∏–≤–∞—é Anchored...")
    for _, obj in pairs(shovel:GetDescendants()) do
        if obj:IsA("BasePart") and obj.Name ~= "Handle" then
            obj.Anchored = false
            print("   üîì –†–∞–∑—ä—è–∫–æ—Ä–µ–Ω:", obj.Name)
        end
    end
    
    wait(0.5)
    
    -- –®–ê–ì 7: –ó–∞–ø—É—Å–∫–∞–µ–º –ø—Ä–æ—Å—Ç—É—é –∞–Ω–∏–º–∞—Ü–∏—é
    print("üé¨ –ó–∞–ø—É—Å–∫–∞—é –∞–Ω–∏–º–∞—Ü–∏—é...")
    startUltraSimpleAnimation(shovel)
    
    print("‚úÖ –£–ª—å—Ç—Ä–∞-–ø—Ä–æ—Å—Ç–∞—è –∑–∞–º–µ–Ω–∞ –∑–∞–≤–µ—Ä—à–µ–Ω–∞!")
    print("üé≠ Shovel —Ç–µ–ø–µ—Ä—å —Å–æ–¥–µ—Ä–∂–∏—Ç –ø–∏—Ç–æ–º—Ü–∞!")
    return true
end

-- –£–õ–¨–¢–†–ê-–ü–†–û–°–¢–ê–Ø –ê–ù–ò–ú–ê–¶–ò–Ø
local function startUltraSimpleAnimation(tool)
    if not tool then return end
    
    print("üé¨ === –ó–ê–ü–£–°–ö –£–õ–¨–¢–†–ê-–ü–†–û–°–¢–û–ô –ê–ù–ò–ú–ê–¶–ò–ò ===")
    
    -- –û—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ–º –ø—Ä–µ–¥—ã–¥—É—â—É—é –∞–Ω–∏–º–∞—Ü–∏—é
    if animationConnection then
        animationConnection:Disconnect()
        animationConnection = nil
    end
    
    -- –ò—â–µ–º Motor6D –¥–ª—è –∞–Ω–∏–º–∞—Ü–∏–∏
    local motors = {}
    for _, obj in pairs(tool:GetDescendants()) do
        if obj:IsA("Motor6D") then
            table.insert(motors, {
                motor = obj,
                originalC0 = obj.C0,
                time = math.random() * 10
            })
            print("   üîó –ù–∞–π–¥–µ–Ω Motor6D: " .. obj.Name)
        end
    end
    
    print(string.format("üé≠ –ù–∞–π–¥–µ–Ω–æ %d Motor6D –¥–ª—è –∞–Ω–∏–º–∞—Ü–∏–∏", #motors))
    
    if #motors > 0 then
        -- Motor6D –∞–Ω–∏–º–∞—Ü–∏—è (–æ—á–µ–Ω—å –ª–µ–≥–∫–∞—è)
        animationConnection = RunService.Heartbeat:Connect(function()
            local time = tick()
            
            for _, data in ipairs(motors) do
                if data.motor and data.motor.Parent then
                    -- –û—á–µ–Ω—å –ª–µ–≥–∫–∞—è –∞–Ω–∏–º–∞—Ü–∏—è —á—Ç–æ–±—ã –Ω–µ –±–ª–æ–∫–∏—Ä–æ–≤–∞—Ç—å –∏–≥—Ä–æ–∫–∞
                    local offsetY = math.sin(time * 1 + data.time) * 0.01
                    local offsetX = math.cos(time * 0.5 + data.time) * 0.005
                    
                    data.motor.C0 = data.originalC0 * CFrame.new(offsetX, offsetY, 0)
                    data.time = data.time + 0.001
                end
            end
        end)
        
        print("‚úÖ –£–ª—å—Ç—Ä–∞-–ª–µ–≥–∫–∞—è Motor6D –∞–Ω–∏–º–∞—Ü–∏—è –∑–∞–ø—É—â–µ–Ω–∞!")
    else
        print("‚ö†Ô∏è Motor6D –Ω–µ –Ω–∞–π–¥–µ–Ω—ã - —Å—Ç–∞—Ç–∏—á–Ω–∞—è –∫–æ–ø–∏—è")
    end
end

